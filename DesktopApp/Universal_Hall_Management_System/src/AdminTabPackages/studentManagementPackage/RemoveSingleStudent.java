    private void studentRegNumberTextFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentRegNumberTextFieldActionPerformed
        // TODO add your handling code here: 
        try {
            initFrame();
            String regNo = studentRegNumberTextField.getText().trim();
            Student student = null;
            student = st = BasicQuery.findStudent(regNo);
            if (student == null) {
                studentNotFound();
                return;
            } else {
                studentFound(student);
                
                int roomNumber = BasicQuery.isAlreadyHasSeat(regNo);
                st.setRoom_number(roomNumber);
                if (roomNumber == 0) {
                    informationLabel.setText("This Student Does Not Has A Seat!!");
                    return;
                } else {
                    informationLabel.setText("Student's Room Number : " + roomNumber);
                    removeStudentButton.setEnabled(true);
                    
                }
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(RemoveSingleStudent.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(RemoveSingleStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_studentRegNumberTextFieldActionPerformed
    
    public void studentFound(Student student) {
        studentName.setText(student.getStudent_name());
        departmentName.setText(student.getStudent_dept());
        studentSession.setText("" + student.getStudent_session());
    }
    
    public void studentNotFound() {
        studentName.setText("No Student Found With This ID!!!");
        departmentName.setText("");
        studentSession.setText("");
    }

