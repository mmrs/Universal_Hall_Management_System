    public void addButtonPressed() {
        if (amountTextField.getText().trim().length() <= 0 && commentTextBox.getText().trim().length() <= 0) {
            JOptionPane.showMessageDialog(this, "Please fill amount and commnet field.");
        } else if (amountTextField.getText().trim().length() <= 0) {
            JOptionPane.showMessageDialog(this, "Please fill amount field.");
        } else if (commentTextBox.getText().trim().length() <= 0) {
            JOptionPane.showMessageDialog(this, "Please fill commnet field.");
        } else {
            int amount = Integer.parseInt(amountTextField.getText().trim());
            String comment = commentTextBox.getText();
            BasicQuery.addToBazar(CurrentUserInfo.USER_ID, amount, comment);
            JOptionPane.showMessageDialog(this,"Amount : " +amount+" Commnet : '"+comment+"' added successfully");
            amountTextField.setText("");
            commentTextBox.setText("");
        }
    }

